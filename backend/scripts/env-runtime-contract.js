function validateEnvRuntimeContract(contract) {
  const errors = [];
  const envKeys = contract.envExampleRequired.map((entry) => entry.key);
  const duplicateEnvKeys = envKeys.filter((key, index) => envKeys.indexOf(key) !== index);
  for (const key of duplicateEnvKeys) {
    errors.push(`Duplicate envExampleRequired key in ENV_RUNTIME_CONTRACT: ${key}`);
  }
  for (const entry of contract.envExampleRequired) {
    if (!/^[A-Z0-9_]+$/.test(entry.key)) {
      errors.push(`Invalid env key format in ENV_RUNTIME_CONTRACT.envExampleRequired: ${entry.key}`);
    }
  }
  for (const [serviceName, keys] of Object.entries(contract.composeRequiredByService)) {
    const duplicates = keys.filter((key, index) => keys.indexOf(key) !== index);
    for (const key of duplicates) {
      errors.push(`Duplicate ${serviceName} compose key in ENV_RUNTIME_CONTRACT: ${key}`);
    }
    for (const key of keys) {
      if (!envKeys.includes(key)) {
        errors.push(`composeRequiredByService.${serviceName} key is missing from envExampleRequired: ${key}`);
      }
    }
  }
  return errors;
}

module.exports = { ENV_RUNTIME_CONTRACT, validateEnvRuntimeContract };
